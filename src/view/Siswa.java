    private void pencarianListener(){
        jTextField1.getDocument().addDocumentListener(new SimpleDocumentListener(){
            @Override
            public void update(DocumentEvent e){
                String kunci = jTextField1.getText();
                DataSiswa ds = new DataSiswa();
                dataSiswa = ds.getCari(kunci);
                tbl = new DefaultTableModel(kolom, baris);
                for(int i=0; i<dataSiswa.length; i++){
                    tbl.addRow(new Object[]{dataSiswa[i][0], dataSiswa[i][1]});
                }
                jTable2.setModel(tbl);
            }
        });
    }
    
    private void onRowSelectedAction(){
        jTable2.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int sRow = jTable2.getSelectedRow();
                    if(sRow != -1){
                        String nis = jTable2.getValueAt(sRow, 0).toString();
                        DataSiswa ds = new DataSiswa();
                        String[] dataSiswa = ds.getSingleData(nis);

                        jTextField2.setText(dataSiswa[0]);//NIS
                        jTextField3.setText(dataSiswa[1]);//Nama
                        jTextArea1.setText(dataSiswa[2]);//Alamat
                        jComboBox3.setSelectedItem(dataSiswa[3]);//Jenis Kelamin
                        jComboBox2.setSelectedIndex(Integer.parseInt(dataSiswa[4]));
                        jComboBox1.setSelectedIndex(Integer.parseInt(dataSiswa[5]));
                    }
                }
            }
        });
    }
    
    private void setTblSiswa(){
        tbl = new DefaultTableModel(kolom, baris);
        DataSiswa obj = new DataSiswa();
        dataSiswa = obj.getDataforTbl();
        for(int i=0; i<dataSiswa.length; i++){
            tbl.addRow(new Object[]{dataSiswa[i][0], dataSiswa[i][1]});
        }
    }
    
