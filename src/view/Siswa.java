    private String[] getAllInput(){
        String[] data = new String[6];
        
        data[0] = jTextField2.getText();//nis
        data[1] = jTextField3.getText();//nama
        data[2] = jTextArea1.getText();//alamat
        data[3] = jComboBox3.getSelectedItem().toString();//Jenis Kelamin
        data[4] = Integer.toString(jComboBox2.getSelectedIndex());//id_jurusan
        data[5] = Integer.toString(jComboBox1.getSelectedIndex());//id_kelas
        
        if(data[3].equals("-- PILIH JENIS KELAMIN --") || data[4].equals("0")
                 || data[5].equals("0") || data[0].equals("")
                 || data[1].equals("")  || data[2].equals("")){
            return null;
        }
        
        
        return data;
    }

