    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nis = jTextField2.getText();
        if(!nis.equals("")){
            DataSiswa ds = new DataSiswa();
            if(ds.isSiswaValid(nis)){
                if(ds.deleteByNis(nis)){
                    setTblSiswa();
                    jTable2.setModel(tbl);
                    clearAll();
                    JOptionPane.showMessageDialog(this, "Delete Siswa Berhasil!"
                    ,"Pesan Delete", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Delete Siswa Gagal!"
                    ,"Pesan Delete", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "NIS tidak Terdaftar!"
                ,"Pesan Delete", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "NIS Kosong, harap Diisi untuk Delete!"
            ,"Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String[] data = getAllInput();
        if(data != null){
            DataSiswa ds = new DataSiswa();
            if(ds.isSiswaValid(data[0])){
                if(ds.updateAllByNis(data)){
                    setTblSiswa();
                    jTable2.setModel(tbl);
                    clearAll();
                    JOptionPane.showMessageDialog(this, "Update Data Siswa Berhasil!"
                    ,"Pesan Update", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Update Data Siswa Gagal!"
                    ,"Pesan Update", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "NIS tidak Terdaftar!"
                ,"Pesan Update", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Input Belum Benar!"
            ,"Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String[] data = getAllInput();
        
        if(data != null){
            DataSiswa ds = new DataSiswa();
            if(!ds.isSiswaValid(data[0])){
                if(ds.insertAll(data)){
                    setTblSiswa();
                    jTable2.setModel(tbl);
                    clearAll();
                    JOptionPane.showMessageDialog(this, "Data Baru Berhasil Dimasukan!"
                    ,"Pesan Insert", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Kesalahan Ketika Memasukan Data!"
                    ,"Pesan Insert", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Harap Masukan NIS yang Berbeda!"
                ,"Pesan Insert", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Input Belum Benar!"
            ,"Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

