    private void onRowSelectedAction(){
        jTable2.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int sRow = jTable2.getSelectedRow();
                    if(sRow != -1){
                        String id = jTable2.getValueAt(sRow, 0).toString();
                        selectedTblId = id;
                        DataPembayaran obj = new DataPembayaran();
                        String[] dataPembayaran = obj.getSingleData(id);
                        
                        jComboBox3.setSelectedItem(dataPembayaran[0]);
                        jTextField3.setText(dataPembayaran[1]);
                        jComboBox5.setSelectedItem(dataPembayaran[3]);
                        jTextField4.setText(dataPembayaran[4]);
                    }
                }
            }
        });
    }
    
