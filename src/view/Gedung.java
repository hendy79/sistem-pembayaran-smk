    private void setCBKelas(){
        DataKelas obj = new DataKelas();
        dataKelas = obj.getNama();
        cbmk.addElement("-- KELAS --");
        for (String dK : dataKelas) {
            cbmk.addElement(dK);
        }
    }
    
    private void setCBNis(){
        DataSiswa obj = new DataSiswa();
        dataSiswa = obj.getDataforTbl();
        cbmn.addElement("-- PILIH NIS --");
        for (String[] dS : dataSiswa) {
            cbmn.addElement(dS[0]);
        }
    }
    
    private void setTblBayar(){
        tbl = new DefaultTableModel(kolom, baris);
        DataPembayaran obj = new DataPembayaran();
        dataBayar = obj.getDataforTbl("Gedung");
        for(int i=0; i<dataBayar.length; i++){
            tbl.addRow(new Object[]{dataBayar[i][0], dataBayar[i][1],
                dataBayar[i][2], dataBayar[i][3], dataBayar[i][4]
                    , dataBayar[i][7]
            });
        }
    }
    
