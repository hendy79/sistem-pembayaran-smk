    private String[] getAllInput(){
        String[] data = new String[4];
        
        data[0] = jComboBox3.getSelectedItem().toString();//nis
        data[1] = DataPetugas.getIDUser();//nip
        String tmp_id_kelas = Integer.toString(jComboBox5.getSelectedIndex());
        DataKelas dk = new DataKelas();
        data[2] = dk.getSingleData(tmp_id_kelas)[2];
        data[3] = "Gedung";//jenis_bayar
        
        if(data[0].equals("-- PILIH NIS --")){
            return null;
        }
        
        return data;
    }
    
